public class Day16 {
    public static void main(String[] args) {
        // Contoh perkalian
        int angka1 = 10;
        int angka2 = 5;
        int hasilKali = angka1 * angka2;

        // Contoh pembagian
        int angka3 = 20;
        int angka4 = 4;
        int hasilBagi = angka3 / angka4;

        System.out.println("Hasil perkalian " + angka1 + " * " + angka2 + " = " + hasilKali);
        System.out.println("Hasil pembagian " + angka3 + " / " + angka4 + " = " + hasilBagi);
    }
}
